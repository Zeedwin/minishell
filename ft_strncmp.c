int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i]
		&& s1[i] != '\0' && s2[i] != '\0' && i < n)
		i++;
	if (i == n)
		return (1);
	else
		return (0);
}
